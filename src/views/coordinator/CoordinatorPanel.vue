<template>
  <div>
    <PanelHeader style="margin-bottom: 2%"/>

    <h3 style="margin-bottom: 2%; padding-left: 1%"> BACKENDDEN KULÜP ADI GELECEK - Koordinatörler </h3>

    <v-sheet class="mx-auto">
      <v-container class="primary" fluid>
        <v-data-table
            :headers="headers"
            :items="users"
            class="elevation-1"
        >
          <template v-slot:top>
            <v-toolbar flat>
              <v-spacer></v-spacer>
              <v-dialog v-model="dialog" max-width="500px">
                <template v-slot:activator="{ on, attrs }">
                  <v-btn
                      color="primary"
                      dark
                      class="mb-2"
                      v-bind="attrs"
                      v-on="on"
                  >
                    Yeni Koordinatör Ekle
                  </v-btn>
                </template>
                <v-card>
                  <v-card-title>
                    <span class="text-h5">{{ formTitle }}</span>
                  </v-card-title>

                  <v-card-text>
                    <v-container>
                      <v-col>
                        <v-row sm="4" md="2">
                          <v-text-field v-model="editedItem.name" label="Ad"/>
                        </v-row>
                        <v-row sm="6" md="4">
                          <v-text-field v-model="editedItem.surname" label="Soyad"/>
                        </v-row>
                        <v-row sm="6" md="4">
                          <v-text-field v-model="editedItem.email" label="E-Mail"/>
                        </v-row>
                        <v-row sm="6" md="4">
                          <v-text-field v-model="editedItem.phoneNumber" label="Telefon Numarası"/>
                        </v-row>
                      </v-col>
                    </v-container>
                  </v-card-text>

                  <v-card-actions>
                    <v-spacer></v-spacer>
                    <v-btn class="accent" text @click="save">
                      Kaydet
                    </v-btn>
                    <v-btn class="secondary" text @click="close">
                      Vazgeç
                    </v-btn>
                  </v-card-actions>
                </v-card>
              </v-dialog>
            </v-toolbar>
          </template>

          <template v-slot:item.actions="{ item }">
            <v-icon small class="mr-2" @click="editItem(item)" >
              mdi-pencil
            </v-icon>
            <v-icon small class="mr-2" @click="deleteItem(item)" >
              mdi-delete
            </v-icon>
            <v-icon small >
              mdi-list-box
            </v-icon>
          </template>

        </v-data-table>
      </v-container>
    </v-sheet>

    <v-dialog v-model="dialogDelete" max-width="500px">
      <v-card>
        <v-card-title class="text-h5">Seçili koordinatörü silmek istediğinizden emin misiniz?</v-card-title>
        <v-card-actions>
          <v-spacer></v-spacer>
          <v-btn class="accent" text @click="deleteItemConfirm">
            Sil
          </v-btn>
          <v-btn class="secondary" text @click="closeDelete">
            Vazgeç
          </v-btn>
          <v-spacer></v-spacer>
        </v-card-actions>
      </v-card>
    </v-dialog>
  </div>
</template>

<script>
import PanelHeader from "@/components/PanelHeader";

export default {
  components: {PanelHeader},
  data () {
    return {
      headers: [
        {text: 'Ad', value: 'name', align: 'center', width:'15%'},
        {text: 'Soyad', value: 'surname', align: 'center'},
        {text: 'E-Mail', value: 'email', align: 'center', sortable: false},
        {text: 'Telefon Numarası', value: 'phoneNumber', align: 'center', sortable: false},
        {text: 'İşlem', value: 'actions', align: 'center', sortable: false}
      ],
      users: [],
      editedIndex: -1,
      editedItem: {
        name: '',
        surname: '',
        email: '',
        phoneNumber: null
      },
      defaultItem: {
        name: '',
        surname: '',
        email: '',
        phoneNumber: null
      },
      dialog: false,
      dialogDelete: false
    }
  },
  methods: {
    editItem(item) {
      this.editedIndex = this.users.indexOf(item)
      this.editedItem = Object.assign({}, item)
      this.dialog = true
    },
    deleteItem(item) {
      this.editedIndex = this.users.indexOf(item)
      this.editedItem = Object.assign({}, item)
      this.dialogDelete = true
    },
    close() {
      this.dialog = false
      this.$nextTick(() => {
        this.editedItem = Object.assign({}, this.defaultItem)
        this.editedIndex = -1
      })
    },
    save() {
      if (this.editedIndex > -1) {
        Object.assign(this.users[this.editedIndex], this.editedItem)
      } else {
        this.users.push(this.editedItem)
      }
      this.close()
    },
    deleteItemConfirm() {
      this.users.splice(this.editedIndex, 1);
      this.closeDelete()
    },
    closeDelete() {
      this.dialogDelete = false
      this.$nextTick(() => {
        this.editedItem = Object.assign({}, this.defaultItem);
        this.editedIndex = -1;
      })
    }
  },
  computed: {
    formTitle() {
      return this.editedIndex === -1 ? 'Koordinatör Ekle' : 'Koordinatör Bilgilerini Düzenle'
    },
  }
}
</script>